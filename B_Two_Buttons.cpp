class Solution {


public:
    // bfs // O(N)
    int solve1(int s, int e) {
        queue<int> q;
        vector<int> dist(3e4, 1e9);

        q.push(s);
        dist[s] = 0;

        while (q.size()) {
            int top = q.front();
            q.pop();

            if (2 * top <= 2 * e && dist[2 * top] > dist[top] + 1) dist[2 * top] = 1 + dist[top], q.push(2 * top);
            if (top - 1 >= 1 && dist[top - 1] > 1 + dist[top]) dist[top - 1] = 1 + dist[top], q.push(top - 1);
        }
        return dist[e];
    }



    // Trick :- O(logN)
    int solve2(int s, int e) {
        int steps = 0;
        while (e > s) {
            e = e % 2 ? e + 1 : e / 2;
            steps++;
        }
        steps += (s - e);
        return steps;
    }
};



