class Solution {
    int query(int k, int h, vi& nums) {
        int damage = 0, n = nums.size();

        for (int i = 0; i < n - 1; i++) {
            damage += min(k, nums[i + 1] - nums[i]);
        }
        damage += k;
        return damage >= h;
    }
public:
    int solve(int n, int h, vi& nums) {
        int l = 1, r = h, ans=0;
        while(l<=r){
            int mid = (l+r)>>1;
            if(query(mid, h, nums) ){
                r = mid-1;
                ans = mid;
            }else{
                l=mid+1;
            }
        }
        return ans;
    }
};



